public class TiposVariaveis{
    public static void main (String[] args){
        // inteiros
        int numberInt = 10;
        // float
        float numberFloat = 5.5F;
        // String
        String firstName = "hikaro";
        //short
        short numberShort = 3200;
        //long
        long numberLong = 1000000000L;

        System.out.println(numberInt+","+numberFloat+","+firstName+","+numberShort+","+numberLong);
    }
}